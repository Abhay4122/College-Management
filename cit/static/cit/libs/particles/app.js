$(function () {
    "use strict";
    var openPopup = $('.btn-open-popup'),
        popupform = $('.popup-form'),
        closeForm = $('.close-form');

    openPopup.on('click', function () {
        popupform.addClass('showed');

        setTimeout(function () {
            popupform.addClass('active');
        }, 350)
    });

    closeForm.on('click', function () {
        popupform.removeClass('active');

        setTimeout(function () {
            popupform.removeClass('showed');
        }, 350)
    });

    var mcInput = $("input.email");

    mcInput.keyup(function (e) {
        var alltxt = mcInput.length;
        var empty = true;
        mcInput.each(function (i) {
            if ($(this).val() == '') {
                empty = true;
                $('#mc-embedded-subscribe').prop('disabled', true);
                return false;
            }
            else {
                empty = false;
            }
        });
        if (!empty) $('#mc-embedded-subscribe').prop('disabled', false);
    });

});